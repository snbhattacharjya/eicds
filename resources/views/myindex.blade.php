                      <li><strong>Pre-school non-formal education</strong> - PSE) is one of the crucial components of the the ICDS scheme. This aims at development of school readiness and a positive attitude towards school education among children of the age group of three to six years through non-formal and joyful play way activities at Anganwadi Centres (AWCs).</li>
                      <li><strong>Nutrition &amp; health education&nbsp;</strong>-  Nutrition and Health Education (NHEd.) forms one of the key elements of the ICDS programmes. Women in the age group of 15-45 years should be given information on their health, nutrition and developmental needs. These are imparted through counselling sessions during home visits, fixed immunization days, VHND (Mamta Diwas), mothers meetings, Gram Sabha and during the panchayat meetings in the community.</li>
                      <li><strong>Immunization</strong> - In 1988 we studied the impact of ICDS on the immunization coverage of children aged 12-24 months and of mothers of infants in 19 rural, 8 tribal, and 9 urban ICDS projects that had been operational for more than 5 years. Complete coverage with BCG, diphtheria-pertussis-tetanus (DPT) and poliomyelitis vaccines was recorded for 65%, 63%, and 64% of children, respectively, in the ICDS population. By comparison, the coverage in the non-ICDS group was only 22% for BCG, 28% for DPT, and 27% for poliomyelitis. Complete immunization with tetanus toxoid was recorded for 68% of the mothers in the ICDS group and for 40% in the non-ICDS group. Coverage was greater in the urban and lower in the tribal projects. Scheduled castes, scheduled tribes, backward communities, and minorities (groups that have a high priority for social services) had immunization coverages in ICDS projects that were similar to those of higher castes.</li>
                      <li><strong>Health check-up</strong> - Popularly known as "Village Health Nutrition Days", it is organized once a month at the Anganwadi Centre. Health functionaries, Auxiliary Nurse Midwife (ANM) and Accredited Social Health Activist (ASHA) visit the anganwadi centre on this day, a basket of services are provided on this day like immunization, micronutrient supplementation, Vitamin A & Folic Acid, deworming, registration of pregnant women, antenatal care, health check-up of sick and undernourished children etc. In addition counselling of pregnant women and care givers is conducted ANM and AWW.</li>
                      <li><strong>Referral services</strong> - Children who are sick or severely undernourished are referred by the Anganwadi worker to ANM or to health facilities.</li>
